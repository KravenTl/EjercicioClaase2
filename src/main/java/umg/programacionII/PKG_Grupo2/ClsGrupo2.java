package umg.programacionII.PKG_Grupo2;

public class ClsGrupo2 {
    //Para calcular el área de una pirámide
    public float triarea(float base,float altura){
        return (base*altura)/2;
    }

    //Para calcular el volumen de una pirámide cuadrada
    public float piravol(float base,float altura){
        return (base*altura)/3;
    }

    //Área de un triángulo
    public float repiaria(float base,float altura){
        return (base*altura)/2;
    }
}
